#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digits[10];
	int		digit_index;

	if (n == 0)
		write(fd, "0", 1);
	else if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
		return ;
	}
	else
	{
		digit_index = -1;
		while (n != 0)
		{
			digits[++digit_index] = n % 10 + '0';
			n /= 10;
		}
		while (digit_index >= 0)
			write(fd, digits + (digit_index--), 1);
	}
}
